DROP DATABASE airlines;
CREATE DATABASE AIRLINES ;
\c airlines;

CREATE TABLE Airline_Companies 
(
	A_name varchar(20),
	A_code varchar(10),
	primary key(A_code) 
);

CREATE TABLE Aeroplanes
(
	Registration_number varchar(10),
	Type varchar(20),
	Airplane_company_code varchar(10),
	primary key (Registration_number),
	foreign key(Airplane_company_code) references Airline_Companies
);

CREATE TABLE Airports
(
	Airport_code integer NOT NULL,
	name varchar (20),
	primary key (Airport_code)
);

CREATE TABLE Routes
(
	Route_no integer NOT NULL,
	Route_name varchar(20),
	primary key(Route_no) 
);

CREATE TABLE Flights
(
	Flight_id integer NOT NULL,
	Arrival integer NOT NULL,
	Departure integer NOT NULL,
	Route integer NOT NULL,
	Airplane_comp_code varchar (10),
	 
	primary key(Flight_id),
	foreign key(Airplane_comp_code) references Airline_Companies,
	foreign key(arrival) references Airports,
	foreign key(departure) references Airports,
	foreign key(Route) references Routes
);

CREATE TABLE Terminals
(
	duty_free integer NOT NULL,
	lounge integer NOT NULL,
	airport integer NOT NULL,

	foreign key(airport) references Airports
);

CREATE TABLE Staff
(
	Employee_id varchar(10),
	name varchar(30),
	type varchar(30),
	f_id integer NOT NULL,

	foreign key(f_id) references Flights ON UPDATE CASCADE ON DELETE CASCADE,
	primary key(Employee_id)
);

CREATE TABLE Arrivals
(
	arr_time varchar(10),
	arr_date varchar(20),
	Flight_id integer NOT NULL,
	apt_code integer NOT NULL,

	foreign key(flight_id) references Flights ON UPDATE CASCADE ON DELETE CASCADE,
	foreign key(apt_code) references Airports
);

CREATE TABLE Departures
(
	dep_time varchar(10),
	dep_date varchar(20),
	Flight_id integer NOT NULL,
	apt_code integer NOT NULL,
	foreign key(flight_id) references Flights ON UPDATE CASCADE ON DELETE CASCADE,
	foreign key(apt_code) references Airports
);

CREATE TABLE Passengers
(
	Passenger_id varchar(10),
	Name varchar(20),
	Contact varchar(10),
	Flight_id integer NOT NULL,
	Airport_code integer NOT NULL,
	date_of_birth varchar(20),
	email varchar(20),
	SEX varchar(5),
	primary key(Passenger_id),

	foreign key(Flight_id) references Flights ON UPDATE CASCADE ON DELETE CASCADE,
	foreign key(Airport_code) references Airports
);

CREATE TABLE Tickets
(
	Fare integer NOT NULL,
	type varchar(20),
	passenger_id varchar(10),
	Route_no integer NOT NULL,

	foreign key(passenger_id) references Passengers ON UPDATE CASCADE on delete cascade,
		foreign key(Route_no) references Routes
);

